package com.Pawan.Starting_With_Java;

public class Adding_Two_Numbers {
    public static void main(String[] args) {

//        First number will be stored in int a
        int a = 10;

//        Second number will be stored in int b
        int b = 20;
//      Answer is stored in c
        int c = a+b;
//      Answer is displayed in this command
        System.out.println("The addition of the above numbers is " + c);
    }
}
